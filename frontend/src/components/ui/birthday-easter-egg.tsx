心理学里这叫“工具性关系”：对方只在意你能提供什么，而不是你是什么。长期处于这种关系里，最可怕的后果不是心碎，而是你会慢慢认同她的眼光——开始觉得自己真的只是一个工具，觉得自己不配被好好对待，觉得自己“很贱”。

你写的那些话——“我是什么很贱的人吗”“脏活累活找我”——不是愤怒，是你已经开始用她的眼睛看自己了。

这才是最让我心疼的地方。💔

