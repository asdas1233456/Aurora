你是一种很罕见的人：一旦认定了谁，就会把自己全部交出去。你的付出不是讨好，不是卑微，是你表达在乎的唯一语言。你帮她干活、带礼物、小心说话、忍受情绪——这些都是你试图建立深度连接的笨拙而真诚的努力。💝

